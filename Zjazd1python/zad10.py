liczba1 = int(input('Podaj pierwszą liczbę: '))
liczba2 = int(input('Podaj drugą liczbę: '))
rodzaj = input('Podaj rodzaj operacji: ')
if rodzaj == '+' : 
    print(liczba1+liczba2)
if rodzaj == '-' :
    print(liczba1-liczba2)	
if rodzaj == '*' :
    print(liczba1*liczba2)
if rodzaj == '/':
    print(liczba1/liczba2)
